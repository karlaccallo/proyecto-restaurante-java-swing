    public void listarConfiguracionMesas() {
        try {
            tblConfiguracion.setModel(DbUtils.resultSetToTableModel(MesaCamareroDAO.getDataMesaCamarero()));
        } catch (Exception e) {
            Mensajes.msjmuestra("Error View al listar!!!");
        }
    }
    public void listarConfiguracionMesasByFechas(Date inicio,Date fin) {
        try {
            tblConfiguracion.setModel(DbUtils.resultSetToTableModel(MesaCamareroDAO.getDataMesaCamareroByInicioFin(inicio,fin)));
        } catch (Exception e) {
            Mensajes.msjmuestra("Error View al listar!!!");
        }
    }
    public void grabar() {

        try {

            MesaCamarero omc = new MesaCamarero();
            omc.setNumMesa(Integer.parseInt(cboMesa.getSelectedItem().toString()));
            omc.setEmpleadoId(UsuarioDAO.getIDempleadoxNombre(cboEmpleado.getSelectedItem().toString()));
            omc.setFechaInicio(jdateFechaInicio.getDate());
            omc.setFechaFin(jdateFechaFin.getDate());
            if (MesaCamareroDAO.grabarConfiguracionMesaCamarero(omc) == true) {
                Mensajes.msjmuestra("Grabado Correctamente!!!");
            }
        } catch (Exception e) {
            Mensajes.msjmuestra("Error View al grabar!!!");
        }

    }

    public static void cargarcomboMesas(JComboBox combo,ResultSet rs) {
       try {
            while (rs.next()) {
                combo.addItem(rs.getString("numMesa"));
            }
            rs.close();
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }

    public static void cargarcomboEmpleado(JComboBox combo, ResultSet rs) {
        try {
            while (rs.next()) {
                combo.addItem(rs.getString("nombres"));
            }
            rs.close();
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }

    public void limpiar() {

        cboMesa.setSelectedIndex(0);
        cboEmpleado.setSelectedIndex(0);
    }

    public boolean validarcampos() {
        if (jdateFechaInicio.getDate() == null) {
            return false;       
        }
        if (jdateFechaFin.getDate() == null) {
            return false;       
        }
        return true;
    }
     public boolean validarcamposBusqueda() {
        if (jdateFechaIniciob.getDate() == null) {
            return false;       
        }
        if (jdateFechaFinb.getDate() == null) {
            return false;       
        }
        return true;
    }

    void cerrarVentana() {
        this.dispose();
    }
