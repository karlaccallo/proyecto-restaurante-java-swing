    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        if (validarcampos()) {
            
            reporteVentasByFechas(jdateFechaIniciob.getDate(),jdateFechaFinb.getDate());
        } else {
            Mensajes.msjmuestra("Ingrese rango de fechas valido");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnDescargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDescargarActionPerformed
       try {
            ExportarExcel obj = new ExportarExcel();
            obj.exportarExcel(tblReporte);
        } catch (IOException ex) {
           Mensajes.msjmuestra("Error al exportar:"+ex.getMessage());
        }
    }//GEN-LAST:event_btnDescargarActionPerformed


    public void reporteVentasByFechas(Date inicio,Date fin) {
        try {
            tblReporte.setModel(DbUtils.resultSetToTableModel(MesaCamareroDAO.getDataReporteVentasByInicioFin(inicio,fin)));
        } catch (Exception e) {
            Mensajes.msjmuestra("Error View al traer el reporte!!!");
        }
    }

    public boolean validarcampos() {
        if (jdateFechaIniciob.getDate() == null) {
            return false;       
        }
        if (jdateFechaFinb.getDate() == null) {
            return false;       
        }
        return true;
    }

    void cerrarVentana() {
        this.dispose();
    }
